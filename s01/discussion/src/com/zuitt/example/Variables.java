package com.zuitt.example;

public class Variables {
    public static void main(String[] args) {
        // Variables act as containers for values that are to be used by the program and manipulated further as needed.
        // In order to create a variable, the data type of the variable must be specified, as well as the identifier for the variable.

        // Declaration vs Initialization

        // Declaration
        int age;
        age = 18;

        // Initialization
        char single = 'a';
        String $_name = "Jane Doe";
        boolean _isGood = true;

        System.out.println(age);
        System.out.println(single);
        System.out.println($_name);
        System.out.println(_isGood);
    }
}
